package com.kodilla.stream.reference;

public class FunctionalCalculator {
    public static double multiplyAByB(final double a, final double b) {
        return a * b;
    }

    public static double addAToB(final double a, final double b) {
        return a + b;
    }

    public static double subBFromA(final double a, final double b) {
        return a - b;
    }

    public static double divideAByB(final double a, final double b) {
        return a / b;
    }
}
